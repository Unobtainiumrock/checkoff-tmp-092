/** Represents a gitlet commit object.
 *  TODO: It's a good idea to give a description here of what else this Class
 *  does at a high level.
 *
 *  @author TODO
 */
public class Commit implements Serializable {
    private String message;
    private String timestamp;
    private String parentHashID; // Generated by a commit self-hashing.
    private List<Map<String, byte[]>> fileHashes;
    private String hashID;
    /**
     * TODO: add instance variables here.
     *
     * List all instance variables of the Commit class here with a useful
     * comment above them describing what that variable represents and how that
     * variable is used. We've provided one example for `message`.
     */
    public Commit (String message, String timestamp, String parentID, List<Map<String, byte[]>> fileHashes) {
        this.message = message;
        this.timestamp = timestamp;
        this.parentHashID = parentID;
        this.fileHashes = fileHashes;
        this.hashID = this.generateHashID();
    }

    public Commit() {
        this.message = "initial commit";
        this.timestamp = "00:00:00 UTC, Thursday, 1 January 1970";
        this.parentHashID = null;
        this.fileHashes = new ArrayList<>();
        this.hashID = sha1(serialize(this), "initial commit");
    }

    private String generateHashID() {
        return sha1(serialize(this), this.message);
    }

    public List<Map<String, byte[]>> getFileHashes() {
        return this.fileHashes;
    }

    public String getMessage() {
        return this.message;
    }

    public String getTimestamp() {
        return this.timestamp;
    }


    public String getHashID() {
        return this.hashID;
    }

    public String getParentID() {
        return this.parentHashID;
    }

    @Override
    public String toString() {
        String res = "";

        res += "Hash ID: " + this.hashID + "\n";
        res += "Parent Hash ID " +  this.parentHashID + "\n";
        res += "Time Stamp: " + this.timestamp + "\n";
        res += "Commit Message: " + this.message + "\n";
        res += "File hashes: ";

        for (Map<String, byte[]> fileHash : fileHashes) {
            String hashStr = fileHash.keySet().iterator().next();
            res += hashStr + "\n";
        }

        return res;
    }

}
